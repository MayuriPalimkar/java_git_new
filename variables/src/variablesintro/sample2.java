package variablesintro;

public class sample2 {
     public static void main (String[] args) {
    	 //main method body
    	 
    	 //1.variable declaration
    	 
    	 String studentfirstname;
    	 String studentlastname;
    	 int rollno;
    	 char studentgrade;
    	 float studentpercentage;
    	 
    	 //2.variable initialization(assigning)
    	 studentfirstname = "viransh";
    	 studentlastname = "handre";
    	 rollno = 15;
    	 studentgrade = 'A';
    	 studentpercentage = 94.20f;
    	 
    	 //3.usages
    	 System.out.println("FirstName :"+ studentfirstname);
    	 System.out.println("LastName :"+ studentlastname);
    	 System.out.println("RollNo :"+ rollno);
    	 System.out.println("Student Grade :"+ studentgrade);
    	 System.out.println("Student Percentage :"+ studentpercentage);
     }
}
