package variablesintro;

public class charfloat {
	public static void main(String[]args) {
		char c1 = 'A';
		char c2 = '8';
		
		System.out.println("char data type-->"+c1);
		System.out.println("char data type-->"+c2);
		
		float f1 = 254.25f;//float data type 4 byte 32 bit
		float f2 = 654;
		
		System.out.println("float data type-->"+f1);
		System.out.println("float data type-->"+f2);
		
		double d1 = 5887965423.225455d;//double data type 8 byte 64 bit
		double d2 = -58745666.258656d;
		double d3 = 4578966552d;
		
		System.out.println("double data tye-->"+d1);
		System.out.println("double data type-->"+d2);
		System.out.println("double data type-->"+d3);
		// boolean data type =1 bit
		// conditional data type
		boolean b1 = true;
		boolean b2 = false;
		
		System.out.println("conditional data type-->"+b1);  //printing statement
		System.out.println("conditional data type-->"+b2);
		
	}

}
