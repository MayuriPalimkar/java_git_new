package forloops;

public class forloop5 {
	public static void main(String[]args) {
		//WAP to print 5 to 1 numbers
		//starting condition=5
		//end condition = 1
		//inc/dec = --
		
		int m;
		for(m=5; m>=1; m--) {
			System.out.println(m);
			
		}
		System.out.println("for loop completed");
	}

}
