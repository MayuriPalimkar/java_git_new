package forloops;

public class forloopadd15 {
	//Write a program to print sum of first 15 numbers using for loop
	public static void main(String[] args){
	     int i;
	     int sum =0;
	   for (i=1; i<=15; i++){
	       sum = sum+ i;
	     
	   }
	   System.out.println("addition of first 15 numbers is-->"+sum);
	}

}
