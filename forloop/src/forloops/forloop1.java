package forloops;

public class forloop1 {
	public static void main(String[]args) {
		/*for loop
		 * to execute multiple lines into single code
		 * syntax-->
		 * for(initialization; condition; inc/dec){
		 * statements
		 * }
		 * 
		 * for(starting condition; ending condition; inc/dec){
		 * code
		 * }
		 */
		//program to write 1 to 5 numbers
		int i= 0;
		for (i=0; i<=5; i++) {
			System.out.println(i);
			
		}
		System.out.println("loop completed");
	}

}
