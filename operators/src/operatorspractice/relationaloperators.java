package operatorspractice;

public class relationaloperators {  //class body
	public static void main (String[]args) {   //main method body
		
		int A = 63;
		int B= 36;
		
		System.out.println(A<B);  //false
		System.out.println(A>B);   //true
		System.out.println(A<=B);  //false
		System.out.println(A>=B);   //true
		System.out.println(A==B);   //false
		System.out.println(A!=B);    //true
		
		
		
	}
	

}
