package whiledowhile;

public class whileloop3 {
	// WAP to print 1 to 30 odd numbers
	/*Syntax-->
	 * initialization
	 * while(condition){
	 * printing statement
	 * inc/dec
	 * }
	 */
  public static void main(String[]args) {
	  int i = 1;
	  while(i<=30) {
		  System.out.println(i);
		  i= i+2;
	  }
  }
}
