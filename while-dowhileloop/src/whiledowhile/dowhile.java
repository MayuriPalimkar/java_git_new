package whiledowhile;

public class dowhile {
   public static void main(String[]args) {
	   //wap to print 1 to 10 numbers using do while loop
	   /*syntax-->
	    * do{
	    * printing statement
	    * inc/dec
	    * }
	    * while(condition)
	    * 
	    */
	   int i=1;
	   do {
		   
		   System.out.println(i);
		   i++;
		   
	   }
	   while(i<=10);
   }
}
