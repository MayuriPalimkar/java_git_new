package whiledowhile;

public class whileloop1 {
	public static void main(String[]args) {
		//WAP to print 10 to 20 numbers using while loop
		
		
		//syntax for while loop
		/* while (condition){
		 * statement
		 * inc/dec
		 * }
		 */
		int i = 10;
		while(i<=20)
		{
			System.out.println(i);
			i++;
		}
	}

}
